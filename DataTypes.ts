//Primitive Data Types: Number, String, Boolean, Null, Undefined

let empid : number = 2345;
let empname : string = "Manish Kumar Gudala"
let empisactive : boolean = true;
let city : null = null;
let price : undefined = undefined;

console.log(empid);
console.log(empname);
console.log(empisactive);
console.log(city);
console.log(price);

//Non Primitive Data Type : Array, Tupple
     //Array
let empno : number[] = [2345, 3456, 4567, 5678, 6789,];
console.log(empno);
     //Tupple - Fixed length and Fixed Data Type
let userdetail : [number, string, boolean] = [3456, "Kavya", true];
console.log(userdetail);

//Any DataType
let salary : any = 1200000;
console.log(salary);

